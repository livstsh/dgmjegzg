// Function to apply a specific style map to the text
function applyStyle(text, styleMap) {
    const lowerMap = Object.keys(styleMap).reduce((acc, key) => {
        acc[key] = styleMap[key];
        return acc;
    }, {});
    
    return Array.from(text).map(char => lowerMap[char] || lowerMap[char.toLowerCase()] || char).join('');
}


