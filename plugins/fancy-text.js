// 11. Strikethrough (Combining character)
function applyStrikethrough(text) {
    // Only applies the combining character to Latin characters and numbers
    return Array.from(text).map(char => {
        // Check if the character is a common Latin letter or number
        if (/[a-zA-Z0-9]/.test(char)) {
            return char + '̶'; // U+0336 Combining Long Stroke Overlay
        }
        return char;
    }).join('');
}

// 12. Underline (Combining character)
function applyUnderline(text) {
    return Array.from(text).map(char => {
        if (/[a-zA-Z0-9]/.test(char)) {
            return char + '̲'; // U+0332 Combining Low Line
        }
        return char;
    }).join('');
}

